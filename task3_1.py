# Инициализируем переменную для суммы
total_sum = 0

print("Вводите числа. Для завершения введите -1")
print("-" * 30)

# Бесконечный цикл, который прервётся при вводе -1
while True:
    # Получаем число от пользователя
    num = int(input("Введите число: "))
    
    # Проверяем, не введено ли -1
    if num == -1:
        break  # Выходим из цикла
        
    # Добавляем число к сумме
    total_sum += num

# Выводим итоговую сумму
print(f"\nИтоговая сумма всех введённых чисел: {total_sum}")