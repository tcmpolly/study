group1 = ["Иванов", "Петров", "Сидоров", "Козлов", "Смирнов", "Васильев", "Николаев", "Орлов", "Белов", "Кузнецов"]
group2 = ["Алексеев", "Федоров", "Макаров", "Андреев", "Павлов", "Семенов", "Григорьев", "Романов", "Волков", "Захаров"]

# 2.1 Создаём спортивную команду (по 5 из каждой группы)
team = tuple(group1[:5] + group2[:5])

# 2.2 Выводим списки и команду
print("Моя группа:", group1)
print("Другая группа:", group2)
print("Спортивная команда:", team)

# 2.3 Выводим длину кортежа
print("Длина команды:", len(team))

# 2.4 Сортируем кортеж по алфавиту
sorted_team = tuple(sorted(team))
print("Отсортированная команда:", sorted_team)

# 2.5 Проверяем наличие "Иванов"
if "Иванов" in team:
    print("Студент Иванов есть в команде")
else:
    print("Студента Иванова нет в команде")

# Количество фамилий "Иванов" в кортеже
count_ivanov = team.count("Иванов")
print("Фамилия 'Иванов' встречается", count_ivanov, "раз(а)")