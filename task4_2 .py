# Получаем число n от пользователя
n = int(input("Введите число n: "))

print(f"\nВычисление суммы факториалов: 1! + 2! + ... + {n}!")
print("-" * 40)

total_sum = 0  # Общая сумма

# Вычисляем сумму факториалов
for i in range(1, n + 1):
    # Вычисляем факториал текущего числа i
    factorial = 1
    for j in range(1, i + 1):
        factorial *= j
    
    # Добавляем к общей сумме
    total_sum += factorial
    
    # Выводим промежуточный результат
    print(f"{i}! = {factorial}")
    print(f"Текущая сумма: {total_sum}")
    print("-" * 20)

# Выводим итоговый результат
print(f"\nИТОГ: 1! + 2! + ... + {n}! = {total_sum}")