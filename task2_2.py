# Получаем код символа от пользователя
code = int(input("Введите код символа ASCII: "))

# Проверяем, является ли код английской буквой
# Заглавные английские буквы: A-Z (коды 65-90)
# Строчные английские буквы: a-z (коды 97-122)
if 65 <= code <= 90:
    print(f"Код {code} соответствует заглавной английской букве '{chr(code)}'")
elif 97 <= code <= 122:
    print(f"Код {code} соответствует строчной английской букве '{chr(code)}'")
else:
    print(f"Код {code} не является английской буквой. Символ: '{chr(code)}'")